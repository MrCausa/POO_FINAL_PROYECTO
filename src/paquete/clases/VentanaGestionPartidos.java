    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
            String textoIndice = txtIndice.getText().trim();

        if (!textoIndice.matches("\\d+")) {
            txtSalida.append("Ingresa un nÃºmero vÃ¡lido en el campo Ã­ndice.\n");
            return;
        }

        int indice = Integer.parseInt(textoIndice) - 1;

        if (indice >= 0 && indice < gestor.getCantidad()) {
            String nombre = txtNombre.getText().trim();
            String siglaStr = txtSigla.getText().trim();
            String representante = txtRepresentante.getText().trim();
            boolean tieneLogo = chkLogo.isSelected();

            if (nombre.isEmpty() || siglaStr.isEmpty() || representante.isEmpty()) {
                txtSalida.append("Completa todos los campos para modificar.\n");
                return;
            }

            char sigla = siglaStr.charAt(0);

            gestor.modificarPartido(indice, nombre, sigla, tieneLogo, representante);
            txtSalida.append("Partido modificado correctamente.\n");
        } else {
            txtSalida.append("Ãndice fuera de rango.\n");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

