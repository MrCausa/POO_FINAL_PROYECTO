    private void configurarTabla() {
        modeloTabla.setColumnIdentifiers(new Object[]{"Índice", "Nombres", "Apellido", "Tipo"});
        tblMiembros.setModel(modeloTabla);
    }

    private void actualizarTabla() {
        modeloTabla.setRowCount(0);
        for (int i = 0; i < gestorMiembros.getPuntero(); i++) {
            MiembroMesa m = gestorMiembros.getMiembros()[i];
            modeloTabla.addRow(new Object[]{i+1, m.getNombres(), m.getApellidos(), m.getTipo()});
        }
    }

