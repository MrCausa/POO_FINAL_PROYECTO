    private void btnRegistrarrActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarrActionPerformed
                if (txtNombre.getText().trim().isEmpty() || txtApellido.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Complete todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        gestorMiembros.registrarMiembro(
            txtNombre.getText().trim(),
            txtApellido.getText().trim(),
            cmbTipo.getSelectedItem().toString()
        );
        
        actualizarTabla();
        txtNombre.setText("");
        txtApellido.setText("");
        JOptionPane.showMessageDialog(this, "Miembro registrado exitosamente");
    }//GEN-LAST:event_btnRegistrarrActionPerformed

