    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = tblMiembros.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un miembro", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        gestorMiembros.eliminarMiembro(fila);
        actualizarTabla();
        JOptionPane.showMessageDialog(this, "Miembro eliminado");
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnAsignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsignarActionPerformed
         int fila = tblMiembros.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un miembro", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        MiembroMesa miembro = gestorMiembros.getMiembros()[fila];
        gestorMesa.asignarMiembro(codigoMesa, miembro);
        JOptionPane.showMessageDialog(this, "Miembro asignado a la mesa " + codigoMesa);
    }//GEN-LAST:event_btnAsignarActionPerformed

